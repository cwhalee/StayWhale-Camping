package vo;

public class Room_Info_Camping {
	
	private String facility_num_c;
	private String reg_num_cr;
	private String facility_name;
	private int price;
	private String room_detail;
	private int standard_amount;
	private String facility_picture;
	private String acc_name;
	
	public String getAcc_name() {
		return acc_name;
	}
	public void setAcc_name(String acc_name) {
		this.acc_name = acc_name;
	}
	public String getFacility_num_c() {
		return facility_num_c;
	}
	public void setFacility_num_c(String facility_num_c) {
		this.facility_num_c = facility_num_c;
	}
	public String getReg_num_cr() {
		return reg_num_cr;
	}
	public void setReg_num_cr(String reg_num_cr) {
		this.reg_num_cr = reg_num_cr;
	}
	public String getFacility_name() {
		return facility_name;
	}
	public void setFacility_name(String facility_name) {
		this.facility_name = facility_name;
	}
	public int getPrice() {
		return price;
	}
	public void setPrice(int price) {
		this.price = price;
	}
	public String getRoom_detail() {
		return room_detail;
	}
	public void setRoom_detail(String room_detail) {
		this.room_detail = room_detail;
	}
	public int getStandard_amount() {
		return standard_amount;
	}
	public void setStandard_amount(int standard_amount) {
		this.standard_amount = standard_amount;
	}
	public String getFacility_picture() {
		return facility_picture;
	}
	public void setFacility_picture(String facility_picture) {
		this.facility_picture = facility_picture;
	}
	
}